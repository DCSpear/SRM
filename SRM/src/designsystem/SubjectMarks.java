    public final void upDateDB(){
	try
	{
		Class.forName("com.mysql.cj.jdbc.Driver");
		sqlConn = DriverManager.getConnection(database,username,password);
		pst = sqlConn.prepareStatement("select * from marks where studentNr = ?");
                pst.setString(1,studentNr);
		rs = pst.executeQuery();

		DefaultTableModel RecordTable = (DefaultTableModel)jTMarks.getModel();
		RecordTable.setRowCount(0);

                rs.next();
                for(int i = 1 ; i < 3 ; i++)
		{
			Vector columnData = new Vector();
                        columnData.add(rs.getString("M" + i)); //depends on colum count
			columnData.add(rs.getString("P" + i)); 
			columnData.add(rs.getString("S" + i)); 
                        columnData.add(rs.getString("E" + i)); 
                        RecordTable.addRow(columnData);
		}
	
	}
	catch(ClassNotFoundException | SQLException ex)
	{
		JOptionPane.showMessageDialog(null, ex);
	}
}
    
