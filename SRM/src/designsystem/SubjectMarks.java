    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Update button, load marks to variables
        int workingRow = SelectedRows + 1;
        String M = jTFMath.getText();
        String P = jTFPhysics.getText();
        String S = jTFSoftwareDesign.getText();
        String E = jTFElectronics.getText();
        
        //make sure a row is selected then update database via MySQL command
        if(SelectedRows == -1)
        {
            JOptionPane.showMessageDialog(this, "Please select a row to edit");
        }
        else
        {
            try
	{
		Class.forName("com.mysql.cj.jdbc.Driver");
		sqlConn = DriverManager.getConnection(database,username,password);
		pst = sqlConn.prepareStatement("UPDATE marks SET M? = ?, P? = ?, S? = ?, E? = ? where studentNr = ?");
                pst.setInt(1,workingRow);
                pst.setString(2, M);
                pst.setInt(3,workingRow);
                pst.setString(4, P);
                pst.setInt(5,workingRow);
                pst.setString(6, S);
                pst.setInt(7,workingRow);
                pst.setString(8, E);
                pst.setString(9, studentNr);
		pst.executeUpdate();
                
                upDateDB();
                JOptionPane.showMessageDialog(this, "Record Updated","Update Complete",JOptionPane.INFORMATION_MESSAGE);
	}
        catch(ClassNotFoundException | SQLException ex)
	{
		JOptionPane.showMessageDialog(null, ex);
	}
            
        }
          
    }//GEN-LAST:event_jButton1ActionPerformed

